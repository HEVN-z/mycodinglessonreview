##
## Python String casefold() Method
##

# Example
# Make the string lower casae:

txt = "Hello, And Welcome To My World!"
x = txt.casefold()
print(x)
# hello, and welcome to my world!

#
# Definition and Usage
#
# The casefold() method returns a string where all the characters are lower case.
# This method is similar to the lower() method, but the casefold() method is stronger, more aggressice, meaning that it will convert more characters into lower case, and will find more matches when comparing two strings and both are converted using the casefold() method.

#
# Syntax
# >> string.casefold()

#
# Parameter Values
#
# No parameters
