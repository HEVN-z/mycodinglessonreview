##
## Python String upper() Method
##

# Example
# Upper case the string:

txt = "Hello my friends"
x = txt.upper()
print(x)
# HELLO MY FRIENDS

#
# Definition and Usage
#
# The upper() method returns a string where all characters are in upper case.
# Symbols and Numbers are ignored.

#
# Syntax
# >> strinig.upper()

#
# Parameter Values
# No parameters

